package org.frameworkset.json;

public class JsonTypeReference {
	public JsonTypeReference(Object typeReference) {
		super();
		this.typeReference = typeReference;
	}

	private Object typeReference;

	public Object getTypeReference() {
		return typeReference;
	}

	public void setTypeReference(Object typeReference) {
		this.typeReference = typeReference;
	}

}
